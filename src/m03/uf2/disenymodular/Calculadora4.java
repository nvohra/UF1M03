class Calculadora4 {
    
    public static int sumar(int op1, int op2) {
        return op1 + op2;
    }
    public static int restar(int op1, int op2) {
        return op1 - op2;
    }
    public static int multiplicar(int op1, int op2) {
        return op1*op2;
    }
    public static int dividir(int op1, int op2) {
        return op1/op2;
    }
}
